// teste lógico ? se verdadeiro : se falso

let n1 = 10
let n2 = 5

let r1 = (!(n1 % 2) ? "Par" : "Impar")    // 0 = False (impar), 1 = True (par)
console.log(r1)

let r2 = (n1 > n2 ? "Verdadeiro" : "Falso")
console.log(r2)

